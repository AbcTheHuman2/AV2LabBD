package model;

public class Resultado {
	
	private String atleta_nome;
	private String pais_nome;
	private float desempenho;
	
	public String getAtletaNome() {
		return atleta_nome;
	}
	
	public void setAtletaNome(String atleta_nome) {
		this.atleta_nome = atleta_nome;
	}
	
	public String getPaisNome() {
		return pais_nome;
	}
	
	public void setPaisNome(String pais_nome) {
		this.pais_nome = pais_nome;
	}
	
	public float getDesempenho() {
		return desempenho;
	}
	
	public void setDesempenho(float desempenho) {
		this.desempenho = desempenho;
	}
}
