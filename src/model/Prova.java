package model;

public class Prova
{
	private int id;
	private String nome;
	private boolean masculino;
	
	public int getId()
	{
		return id;
	}
	
	public void setId(int id)
	{
		this.id = id;
	}
	
	public String getNome()
	{
		return nome;
	}
	
	public void setNome(String nome)
	{
		this.nome = nome;
	}
	
	public boolean getMasculino()
	{
		return masculino;
	}
	
	public void setMasculino(boolean masculino)
	{
		this.masculino = masculino;
	}
}
